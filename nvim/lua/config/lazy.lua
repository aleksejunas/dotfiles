-- At the end of your config/init.lua
_G.lazy_startup_time = nil

vim.api.nvim_create_autocmd("User", {
  pattern = { "LazyVimStarted", "LazyDone" },
  callback = function()
    local ok, lazy = pcall(require, "lazy")
    if ok then
      local stats = lazy.stats()
      local startup_time = stats.startuptime
      if not startup_time or startup_time == 0 then
        startup_time = stats.times and stats.times.LazyStart or nil
      end
      if startup_time then
        _G.lazy_startup_time = math.floor(startup_time * 100) / 100
        print(">>> _G.lazy_startup_time set to", _G.lazy_startup_time)
        if package.loaded["alpha"] then
          require("alpha").redraw()
        end
      else
        print(">>> Could not determine startup time!")
      end
    end
  end,
})
